def computeNormal(v1, v2, v3):
	newV1 = v2[0]-v1[0], v2[1]-v1[1], v2[2]-v1[2]
	newV2 = v3[0]-v2[0], v3[1]-v2[1], v3[2]-v2[2]

	x1, y1, z1 = newV1
	x2, y2, z2 = newV2

	v1Norm = reduce(add, [x*x for x in newV1])
	v2Norm = reduce(add, [x*x for x in newV2])

	xVect = [(y1*z2 - z1*y2), -(x1*z2 - z1*x2), (x1*y2 - y1*x2)]
	xVNorm = sqrt(reduce(add, [x*x for x in xVect]))

	glNormal3f(xVect[0]/xVNorm, xVect[1]/xVNorm, xVect[2]/xVNorm)

	return [x/xVNorm for x in xVect]

